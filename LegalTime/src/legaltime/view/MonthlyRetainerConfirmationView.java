public void actionPerformed(ActionEvent e) {
        if("ApproveSelection".equals(e.getActionCommand())){
            if(dcEffectiveDate.getDate() == null){
               JOptionPane.showConfirmDialog(this, "Please select a date to apply charges","Must choose a date...",JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            this.setVisible(false);
            selectionConfirmed = true;

        }else if("CancelSelection".equals(e.getActionCommand())){
             this.setVisible(false);
            selectionConfirmed = false;
        }


    }

    public boolean isSelectionConfirmed() {
        return selectionConfirmed;
    }
