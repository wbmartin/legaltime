    public void formatTblPaymentLog(){
         TableColumn tc;
         //Effective_date
         tc = tblPaymentLog.getColumnModel().getColumn(0);
         tc.setPreferredWidth(75);
         tc.setMinWidth(25);
         tc.setMaxWidth(100);
         tc.setCellRenderer(DateTableCellRenderer.getInstance());
         tc.setCellEditor(new DateTableCellEditor());
         //Description
         tc = tblPaymentLog.getColumnModel().getColumn(1);
         tc.setPreferredWidth(200);
         tc.setMinWidth(25);
         tc.setMaxWidth(400);
         //Amount
         tc = tblPaymentLog.getColumnModel().getColumn(2);
         tc.setPreferredWidth(50);
         tc.setMinWidth(25);
         tc.setMaxWidth(75);
         tc.setCellRenderer(CurrencyTableCellRenderer.getInstance());
         
    }

